
package com.proyecto.persona;




public class Granjero {
    private int oro;
    private int vida;
    private String nombre;

    public Granjero(int oro, int vida, String nombre) {
        this.oro = oro;
        this.vida = vida;
        this.nombre = nombre;
    }

    public int getOro() {
        return oro;
    }

    public void setOro(int oro) {
        this.oro = oro;
    }

    public int getVida() {
        return vida;
    }

    public void setVida(int vida) {
        this.vida = vida;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

   
    


}
